#Example of Assignment operator in pyhton

num1 = int(input("Enter num1 : "))
num2 = int(input("Enter num2 : ")) 

num1 += num2
print(f" += operator num1 : {num1}")

num1 -= num2
print(f" -= operator num1 : {num1}")

num1 *= num2
print(f" *= operator num1 : {num1}")

num1 /= num2
print(f" /= operator num1 : {num1}")

num1 %= num2
print(f" %= operator num1 : {num1}")

num1 **= num2
print(f" **= operator num1 : {num1}")

num1 //= num2
print(f" //= operator num1 : {num1}")