#Example of Relational Operators in Python
num1 = int(input("Enter num1 : "))
num2 = int(input("Enter num2 : "))

#Equal to Operator (==)
equal_to = (num1 == num2)
print(f"Equal_to : {equal_to}")

#Not Equal to Operator (!=)
not_equal_to = (num1 != num2)
print(f"Not_Eqaul_to : {not_equal_to}")

#Greater then Operator (>)
greater_than = (num1 > num2)
print(f"Greater_than : {greater_than}")

#Less than Operator (<)
less_than = (num1 < num2)
print(f"Less_than : {less_than}")

#Greater than or Equal to Operator (>=)
greater_than_equal_to = (num1 >= num2)
print(f"Greater_than_equal_to : {greater_than_equal_to}")

#Less than or Equal to Operator (<=)
less_than_equal_to = (num1 <= num2)
print(f"Less_than_equal_to : {less_than_equal_to}")

